	/**
	 * Retrieve the carbon dioxide
	 * 
	 * @param amount
	 * @return
	 */
	private boolean retrieveCO2(double amount) {
		boolean result = false;
		if (co2Cache - amount < -co2Threshold) {
			result = retrieve(co2Cache, CO2_ID, true);
			co2Cache = -amount;
		}
		else {
			co2Cache -= amount;
		}
		return result;
	}
	
